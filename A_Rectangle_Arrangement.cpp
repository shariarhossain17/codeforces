void solve()
{
    int n;
    cin >> n;

    int w = 0, h = 0;

    for (int i = 0; i < n; i++)
    {

        int wi, hi;
        cin >> wi >> hi;
        w = max(w, wi);
        h = max(h, hi);
    }

    cout << 2 * (w + h) << endl;
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}